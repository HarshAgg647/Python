a = input("Enter the first number: ")
b = input("Enter the second number: ")

# Adding two numbers
sum = float(a) + float(b)
print("Addition: ",sum)

# Subtracting two numbers
sub = float(a) - float(b)   
print("Subtraction: ",sub)  

# Multiplying two numbers
mul = float(a) * float(b)   
print("Multiplication: ",mul)

# Dividing two numbers
div = float(a) / float(b)
print("Division: ",div)